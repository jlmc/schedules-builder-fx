    /**
     * Creates the icon button.
     * @param iconName
     *            the icon name
     * @param text
     *            the text
     * @param iconSize
     *            the icon size
     * @return the button
     */
    public static Button createIconButton(final String iconName, final String text,
            final int iconSize) {
        final Label icon = createIconLabel(iconName);
        icon.setStyle("-fx-font-size: " + iconSize + "px;");
        final Button b = new Button(text, icon);
        // //return ButtonBuilder.create().text(text).graphic(icon).build();
        return b;
    }

